    else {
        URL = [NSURL URLWithString:[NSString stringWithFormat:@"http://google.com/search?q=%@", [URLString stringByAddingPercentEscapesUsingEncoding:NSASCIIStringEncoding]]]; // Uses %20 etc instead of + to encode the spaces
    }
    
